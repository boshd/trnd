import UIKit

extension UIColor {
    
    static func editProfileGray() -> UIColor {
        return UIColor(red: 233/255.0, green: 235/255.0, blue: 235/255.0, alpha: 1.0)
    }
    
    static func followingGreen() -> UIColor {
        return UIColor(red: 87/255.0, green: 180/255.0, blue: 34/255.0, alpha: 1.0)
    }
    
    static func neonBlue() -> UIColor {
        return UIColor(red:0.15, green:0.83, blue:0.95, alpha:1.0)
    }
    
    static func fabGold() -> UIColor {
        return UIColor(red:0.83, green:0.69, blue:0.22, alpha:1.0)
    }
    
    static func fabishPink() -> UIColor {
        return UIColor(red:0.96, green:0.36, blue:0.41, alpha:1.0)
    }
    
    static func fabishGreen() -> UIColor {
        return UIColor(red:0.44, green:1.00, blue:0.91, alpha:1.0)
    }
}
